const PI = Math.PI;

console.log(PI);

console.log(Math.round(PI)); // 3 Rounds down or up to the nearest integer

console.log(Math.floor(PI)); // 3 Rounds down to the nearest integer

console.log(Math.ceil(PI)); // 4 Rounds up to the nearest integer

console.log(Math.trunc(PI)); // 3 Removes the decimal part

const randNum = Math.random(); // Generates a random number between 0 and 1

